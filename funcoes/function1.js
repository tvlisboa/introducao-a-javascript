function parimpar(n) {
    if (n % 2 == 0) {
        return "Par"
    } else {
        return "Impar"
    }
}

let result1 = parimpar(7)
let result2 = parimpar(772)
let result3 = parimpar(10)
let result4 = parimpar(5)
let result5 = parimpar(99)
