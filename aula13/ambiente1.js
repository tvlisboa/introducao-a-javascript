
var cont = 5;

/**
 * Teste logico no inicio
 */

while (cont < 10) {
    console.log(`Passo ${cont} `)
    cont++
}

/**
 * Teste logico no final
 */

do {
    cont++
    console.log(" ")
    console.log(`Passo ${cont}`)
} while (cont < 12)

for (let i = 0; i < 10; i++) {
    console.log(`Passo ${cont}`)

}